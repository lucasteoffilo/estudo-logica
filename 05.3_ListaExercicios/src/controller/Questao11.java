package controller;

public class Questao11 {

	/*
	 * Desenvolver um programa que triangule uma matriz de ordem 10.
	 */
	public static void main(String[] args) {
		
		int a, b;
		
		System.out.println("Triangulação de matriz de ordem 10");
		for (a = 1; a <= 10; a++) {
			
			for (b = 1; b <= 10; b++) { 
				System.out.println(a + "-" + b);			
				
			}
		
		}

	}

}
